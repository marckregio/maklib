package com.marckregio.makunatlib.util;

import android.content.Context;
import android.os.AsyncTask;
import android.util.Log;


import com.marckregio.makunatlib.Files;

import java.io.BufferedInputStream;
import java.io.ByteArrayOutputStream;
import java.io.File;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;
import java.net.HttpURLConnection;
import java.net.MalformedURLException;
import java.net.URL;
import java.net.URLConnection;

/**
 * Created by makregio on 01/02/2017.
 */

public class DownloadFiles extends AsyncTask<String, Integer, String> {

    public Context context;
    String filename;
    static String OUTPUT = "";

    public DownloadFiles(Context context, String filename){
        this.context = context;
        this.filename = filename;
        OUTPUT = Files.getMainDir();
    }

    @Override
    protected String doInBackground(String... sUrl) {
        InputStream in = null;
        BufferedInputStream input = null;
        OutputStream output = null;
        HttpURLConnection connection = null;
        try {
            URL url = new URL(sUrl[0]);
            connection = (HttpURLConnection) url.openConnection();
            connection.connect();

            if (connection.getResponseCode() != HttpURLConnection.HTTP_OK) {
                return "Server returned HTTP " + connection.getResponseCode()
                        + " " + connection.getResponseMessage();
            }

            // this will be useful to display download percentage
            // might be -1: server did not report the length
            int fileLength = connection.getContentLength();

            // download the file
            in = connection.getInputStream();
            input = new BufferedInputStream(in);
            output = new FileOutputStream(OUTPUT + filename + ".zip");

            byte data[] = new byte[4096];
            long total = 0;
            int count;
            while ((count = input.read(data)) != -1) {
                // allow canceling with back button
                if (isCancelled()) {
                    input.close();
                    return null;
                }
                total += count;
                // publishing the progress....
                if (fileLength > 0) // only if total length is known
                    publishProgress((int) (total * 100 / fileLength));
                output.write(data, 0, count);
            }
        } catch (Exception e) {
            return e.toString();
        } finally {
            try {
                if (output != null)
                    output.close();
                if (input != null)
                    input.close();
            } catch (IOException ignored) {
            }

            if (connection != null)
                connection.disconnect();
        }
        return null;
    }

}
